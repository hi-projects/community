package com.example.community.constants;

public class DateTimeConstants {
    // Unit: millisecond
    public static final int ONE_SECOND_MILLI = 1000;
    public static final int ONE_MINUTE_MILLI = 60 * ONE_SECOND_MILLI;
    public static final int ONE_HOUR_MILLI = 60 * ONE_MINUTE_MILLI;
    public static final long ONE_DAY_MILLI = 24 * ONE_HOUR_MILLI;
    public static final long ONE_WEEK_MILLI = 7 * ONE_DAY_MILLI;
    public static final long ONE_MONTH_MILLI = 30 * ONE_DAY_MILLI;

    // Unit: second
    public static final int ONE_SECOND_SECOND = 1;
    public static final int ONE_MINUTE_SECOND = 60 * ONE_SECOND_SECOND;
    public static final int ONE_HOUR_SECOND = 60 * ONE_MINUTE_SECOND;
    public static final int ONE_DAY_SECOND = 24 * ONE_HOUR_SECOND;
    public static final int ONE_WEEK_SECOND = 7 * ONE_DAY_SECOND;
    public static final int ONE_MONTH_SECOND = 30 * ONE_DAY_SECOND;

}
